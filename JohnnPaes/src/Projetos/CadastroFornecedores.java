    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        
         DefaultTableModel modelo = (DefaultTableModel) TableFornecedores.getModel();
        modelo.setNumRows(0);
        FornecedoresDAO dao = new FornecedoresDAO();
        for(Fornecedores f : dao.pesquisa()){
            modelo.addRow(new Object[]{f.getEmpresa(),f.getNomeContato(),f.getCNPJ(),f.getTelefone(),f.getCelular(),
            });
        }
          txtNomeEmpresa.setText("");
        txtNomeContato.setText("");
        txtCNPJ.setText("");
        txtTelefone.setText("");
        txtCelular.setText("");
        
       
         txtCNPJ.enable(true);
    }//GEN-LAST:event_btnConsultarActionPerformed

