    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
       //  jButton3.setText("Cadastrar");
      
        if(txtNomeEmpresa.getText().isEmpty() || txtCNPJ.getText().isEmpty() || txtNomeContato.getText().isEmpty() || txtCelular.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Prencha os campos");
       }
        else{
        Fornecedores fornecedores = new Fornecedores();
        FornecedoresDAO adao = new FornecedoresDAO();
        
        fornecedores.setEmpresa(txtNomeEmpresa.getText());
        fornecedores.setNomeContato(txtNomeContato.getText());
        fornecedores.setCNPJ(txtCNPJ.getText());
        fornecedores.setTelefone(txtTelefone.getText());
        fornecedores.setCelular(txtCelular.getText());
        adao.create(fornecedores);
        
        txtNomeEmpresa.setText("");
        txtNomeContato.setText("");
        txtCNPJ.setText("");
        txtTelefone.setText("");
        txtCelular.setText("");
        
         txtCNPJ.enable(true);
        
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

