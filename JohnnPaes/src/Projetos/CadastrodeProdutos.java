    private void btnPesqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesqActionPerformed
        
        Connection conn = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;
            
        try {
        
            conn = connectionFactory.getConnection();
            stmt = conn.prepareStatement("select id,empresa,CNPJ from Fornecedores where empresa like '%" + txtEmpresa.getText()+"%'");
            rs = stmt.executeQuery();
            
            DefaultTableModel tabela = (DefaultTableModel) tableEmpresa.getModel();
            
            tabela.setNumRows(0);
            
            while(rs.next()){
                tabela.addRow(new Object[] {rs.getString("id"),
                                            rs.getString("empresa"),
                                            rs.getString("CNPJ")});
            }
            
            connectionFactory.closeConnection(conn, stmt, rs);
            
            txtEmpresa.setText("");
        }
        catch (Exception e){
           JOptionPane.showMessageDialog(null,"Não conectado" + e.getMessage());
        }
        
    }//GEN-LAST:event_btnPesqActionPerformed

