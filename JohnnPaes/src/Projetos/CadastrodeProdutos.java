    private void btnConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultaActionPerformed
        
        Connection conn = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;
            
        try {
        
            conn = connectionFactory.getConnection();
            stmt = conn.prepareStatement("select * from prod_fornece");
            rs = stmt.executeQuery();
            
            DefaultTableModel tabela = (DefaultTableModel) tableProd.getModel();
            
            tabela.setNumRows(0);
            
            while(rs.next()){
                tabela.addRow(new Object[] {rs.getString("id"),
                                            rs.getString("nome"),
                                            rs.getString("descricao"),
                                            rs.getString("tipo"),
                                            rs.getString("empresa")});
            }
            connectionFactory.closeConnection(conn, stmt, rs);
            
            txtProd.setText("");
        }
        catch (Exception e){
           JOptionPane.showMessageDialog(null,"Não conectado" + e.getMessage());
        }
        txtCodProd.setText("");
        txtDescricao.setText("");
        txtEmpresa.setText("");
        txtForne.setText("");
        txtProd.setText("");
        txtTipo.setText("");
    }//GEN-LAST:event_btnConsultaActionPerformed

